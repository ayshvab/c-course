isize utf8_from(enum encoding encoding, u8 *src, isize src_len, u8 *dest) {
  u32 *table = NULL;
  isize table_len = 0;
  if (encoding == encoding_cp1251) {
    table = cp1251_to_unicode_table;
    table_len = array_len(cp1251_to_unicode_table);
  } else if (encoding == encoding_koi8) {
    table = koi8_to_unicode_table;
    table_len = array_len(koi8_to_unicode_table);
  } else if (encoding == encoding_iso_8859_5) {
    table = iso_8859_5_to_unicode_table;
    table_len = array_len(iso_8859_5_to_unicode_table);
  }

  isize dest_len = 0;
  for (isize i = 0; i < src_len; i++) {
    if (src[i] >= table_len) {
      printf("[ERROR]: invalid codepoint for this encoding\n");
      exit(-1);
    }
    u32 codepoint = table[src[i]];
    if (codepoint < 0x80) {
      dest[dest_len] = src[i];
      dest_len++;
    } else if (codepoint < 0x800) {
      u8 byte = 0xC0 | ((0x7 & (codepoint >> 8)) << 2);
      byte |= 0x3 & (codepoint >> 6);
      dest[dest_len++] = byte;
      byte = 0x80 | ((0x3 & (codepoint >> 4)) << 4);
      byte |= (0xF & codepoint);
      dest[dest_len++] = byte;
    } else {
      assert(0);
    }
  }
  return dest_len;
}

void app_run(isize memory_capacity, enum encoding source_encoding, char *source_filename, char *dest_filename) {
  struct buffer buffer = {0};
  buffer.cap = memory_capacity;
  buffer.data = malloc(buffer.cap);
  if (buffer.data == NULL) {
    handle_error();
  }

  FILE *file = fopen(source_filename, "rb");
  if (file == NULL) {
    handle_error();
  }
    
  size_t bytes_read = 0;
  while ((bytes_read = fread(buffer.data+buffer.len, 1, buffer.cap-buffer.len, file)) > 0) {
    if (ferror(file)) {
      handle_error();
    }
    buffer.len += bytes_read;
  }

  u8 *output_buffer = &buffer.data[buffer.len];
  isize output_buffer_len = 0;
  if (source_encoding == encoding_cp1251) {
    output_buffer_len = utf8_from(encoding_cp1251, buffer.data, buffer.len, output_buffer);
  } else if (source_encoding == encoding_iso_8859_5) {
    output_buffer_len = utf8_from(encoding_iso_8859_5, buffer.data, buffer.len, output_buffer);
  } else if (source_encoding == encoding_koi8) {
    output_buffer_len = utf8_from(encoding_koi8, buffer.data, buffer.len, output_buffer);
  }
  
  if (output_buffer_len == 0) {
    return;
  };

  FILE *out = fopen(dest_filename, "wb");
  if (out == NULL) {
    handle_error();
  }

  size_t total_bytes_write = 0;
  size_t bytes_write = 0;
  while ((bytes_write = fwrite(&output_buffer[total_bytes_write], 1, output_buffer_len-total_bytes_write, out)) > 0) {
    if (ferror(file)) {
      handle_error();
    }
    if (feof(file)) {
      break;
    }
    total_bytes_write += bytes_write;
  }
}

int main(int argc, char **argv) {  
  if (argc != 4) {
    printf("Usage: convert-to-utf8 source_encoding source_filename utf8_output.txt\n");
    return 0;
  }

  enum encoding source_encoding = encoding_unknown;
  if (strcmp(argv[1], "cp1251") == 0) {
    source_encoding = encoding_cp1251;
  } else if (strcmp(argv[1], "iso-8859-5") == 0) {
    source_encoding = encoding_iso_8859_5;
  } else if (strcmp(argv[1], "koi8") == 0) {
    source_encoding = encoding_koi8;
  }
  
  if (source_encoding == encoding_unknown) {
    printf("[ERROR]: Unknown encoding\n");
    return 1;
  }
  
  app_run(1*GB, source_encoding, argv[2], argv[3]);
  
  return 0;
}

